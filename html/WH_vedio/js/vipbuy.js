$(function(){
    //搜索功能
    $(".btn1").click(function(){
        var Word=$("#q").val();
        if(Word){
            window.location.href="http://img.10xunc.com/备用件/html/search.html?keyWord="+Word;
        }
        
    });
    $("#q").keyup(function(e){
        if(e.keyCode==13){
            var Word=$("#q").val();
            if(Word){
                window.location.href="http://img.10xunc.com/备用件/html/search.html?keyWord="+Word;
            }
        }
    });
    //
    $("div.list").click(function(){
        $(this).addClass("ac").siblings().removeClass("ac");
        $("div.list .dui").removeClass("ac");
        $("div.ac .dui").addClass("ac");
        
    })
    






})